package com.smartneck.twofive.Fit.util.User;

public class Fit_Age {
    int age;
    float ageWeight;

    public Fit_Age(int age, float ageWeight) {
        this.age = age;
        this.ageWeight = ageWeight;
    }

    public int getAge() {
        return age;
    }

    public float getAgeWeight() {
        return ageWeight;
    }
}
