package com.smartneck.twofive.util.User;

public class Age {
    int age;
    float ageWeight;

    public Age(int age, float ageWeight) {
        this.age = age;
        this.ageWeight = ageWeight;
    }

    public int getAge() {
        return age;
    }

    public float getAgeWeight() {
        return ageWeight;
    }
}
